// Animate numbers (if you want to add stats later)
function animateValue(element, start, end, duration) {
    let startTimestamp = null;
    const step = (timestamp) => {
        if (!startTimestamp) startTimestamp = timestamp;
        const progress = Math.min((timestamp - startTimestamp) / duration, 1);
        element.textContent = Math.floor(progress * (end - start) + start);
        if (progress < 1) {
            window.requestAnimationFrame(step);
        }
    };
    window.requestAnimationFrame(step);
}

// Add active state to nav links based on scroll position
window.addEventListener('scroll', () => {
    const sections = document.querySelectorAll('section[id]');
    const scrollY = window.pageYOffset;

    sections.forEach(section => {
        const sectionHeight = section.offsetHeight;
        const sectionTop = section.offsetTop - 100;
        const sectionId = section.getAttribute('id');
        const navLink = document.querySelector(`.nav-link[href="#${sectionId}"]`);

        if (navLink && scrollY > sectionTop && scrollY <= sectionTop + sectionHeight) {
            document.querySelectorAll('.nav-link').forEach(link => {
                link.classList.remove('active');
            });
            navLink.classList.add('active');
        }
    });
});

// Add loading animation
window.addEventListener('load', () => {
    document.body.classList.add('loaded');
});

// Skill badge expand/collapse functionality
document.querySelectorAll('.skill-toggle').forEach(button => {
    button.addEventListener('click', function(e) {
        e.stopPropagation();
        const badge = this.closest('.skill-badge');
        const isExpanded = badge.classList.contains('expanded');
        const toggleText = this.querySelector('.toggle-text');
        
        // Toggle current badge
        badge.classList.toggle('expanded');
        
        // Update button text
        if (isExpanded) {
            toggleText.textContent = 'Show more';
        } else {
            toggleText.textContent = 'Show less';
        }
    });
});
